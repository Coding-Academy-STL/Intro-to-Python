# The console is the black box on the right.
# If you type python in the console, it will start up an interactive Python session.
# You know you are in a PYTHON console when it shows the >>>
# Instead of the orange thing
# You can then type in code and Python will run it as you go


# Try typing 2 + 2 in the console
2 + 2


# When you type something in the console, Python will automatically print it out
# For example, if we type in the console:
9 + 10
# Prints 19 automatically, but ONLY in the console

# If you click RUN, Python will not print stuff out unless you use print( )
# Type in C to run this file.

1729 - 42
# Prints 1687 when typed in the console
# Does nothing when you run this file

print(1729 - 42)
# Prints 1687 when typed in the console, but it isn't necessary to use print( ) in the console
# Prints 1687 as well when you run this file


# You can use the console any time you want to test some lines of code
# Just type python and if you see the >>> you've started an interactive Python session

# For example, let's say we want to find the type of a variable
# Type these lines into the console, one at a time
name = "Billiam"
type(name)
age = 13
type(age)


